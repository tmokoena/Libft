#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char			*ptr;
	unsigned int	i[2];

	i[0] = ft_strlen(s1);
	i[1] = 0;
	ptr = s1;
	while (s2[i[1]] != '\0' && i[1] < n)
	{
		ptr[i[0]] = s2[i[1]];
		i[0]++;
		i[1]++;
	}
	ptr[i[0]] = '\0';
	return (ptr);
}
