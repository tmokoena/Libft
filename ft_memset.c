#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	int		i;

	i = len - 1;
	ptr = (char *)b;
	while (i >= 0)
	{
		ptr[i] = c;
		i--;
	}
	return (ptr);
}
