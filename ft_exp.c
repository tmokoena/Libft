#include "libft.h"

int		ft_exp(int x, int pow)
{
	int ans;

	ans = x;
	if (pow == 0)
		return (1);
	pow--;
	while (pow)
	{
		ans = ans * x;
		pow--;
	}
	return (ans);
}
